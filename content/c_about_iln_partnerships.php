
	<h1>ABOUT ILN PARTNERSHIPS</h1>
	<h3>Matching participants</h3>
	<p>The ILN will use the information provided by prospective participants to match them with other participants, to form a partnership. We will look at things like the sector that they work in, location, career stage, and areas of interest. The more information we have the better the match will be, however if applicants are very particular in the type of person they would like to be partnered with it may be difficult for us to find the perfect match for you.
	</p>
	<p>Note that we will not automatically match people who are similar – if one works in a public library and indicates that they would like to be partnered with someone in an academic library, we will try our best to meet this request. Likewise, if an applicant  is at a later stage in their career and is eager to be partnered with an early-career practitioner, we will try and match them with someone that meets that description rather than someone who is also in a later career stage.
	</p>
	<h3>Meeting ILN partners</h3>
	<p>Once we have matched partners, we provide information to both along with guidelines on how to maintain a healthy <a href="http://interlibnet.org/frequently-asked-questions-2/#1">peer mentoring</a> relationship. If we are unable to secure a suitable match we will contact the applicant to explore other options.
	</p>
	<p>It’s up to each of the participants involved to make the time to get to know their ILN partner. Distance communication is tricky, especially when you are just getting to know someone, so it is important to communicate clearly as well as to set expectations for the partnership. Tips we can suggest in the early stages of a partnership are:
	</p>
		<ul>
		<li>Establish the partnership by providing contact details and other information to both participants</li>
		<li>Provide partners with guidelines on how to communicate effectively</li>
		<li>Regularly touch base with partners to ensure that the relationship is working for both participants</li>
		<li>Provide monthly discussion topics to partners to stimulate the relationship</li>
		<li>Host online discussions to allow you to develop wider networks with other participants</li>
		</ul>
	<p>Discussion topics will change regularly and we aim to present topics that everyone can relate to.  We are very eager to hear any suggestions for discussion topics.
	</p>
	<h3>Concluding partnerships</h3>
	<p>Partnerships are facilitated by the ILN for four months. We find that this is long enough to get to know your partner, but also allows you a chance to move on to another partnership and develop wider networks.
	</p>
	<p>Participants will receive a reminder one month before your partnership is due to expire. This will give participants a chance to discuss whether they would like to continue their relationship outside of the facilitated program structure. There is no requirement to cease partnerships once the facilitation ends, however formal ILN support will cease.
	</p>
	<p>Once formal partnerships have concluded, everyone is welcome to apply for subsequent partnership rounds.
	</p>

		
		
